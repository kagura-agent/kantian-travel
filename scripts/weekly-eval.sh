#!/usr/bin/env bash
# weekly-eval.sh ‚ÄĒ Lightweight self-eval, 3 proxy metrics
# Usage: bash weekly-eval.sh [YYYY-MM-DD]  (date = end of week, default today)
# Output: wiki/eval/YYYY-WXX.md

set -euo pipefail

WORKSPACE="${HOME}/.openclaw/workspace"
MEMORY_DIR="${WORKSPACE}/memory"
BELIEFS="${WORKSPACE}/beliefs-candidates.md"
EVAL_DIR="${WORKSPACE}/wiki/eval"

END_DATE="${1:-$(date +%Y-%m-%d)}"
# Calculate week number
WEEK_NUM=$(date -d "$END_DATE" +%V 2>/dev/null || date -j -f "%Y-%m-%d" "$END_DATE" +%V)
YEAR=$(date -d "$END_DATE" +%Y 2>/dev/null || date -j -f "%Y-%m-%d" "$END_DATE" +%Y)

# 7 days back
START_DATE=$(date -d "$END_DATE - 6 days" +%Y-%m-%d 2>/dev/null || date -j -v-6d -f "%Y-%m-%d" "$END_DATE" +%Y-%m-%d)

mkdir -p "$EVAL_DIR"
OUT="${EVAL_DIR}/${YEAR}-W${WEEK_NUM}.md"

echo "ūüďä Eval: ${START_DATE} ‚Üí ${END_DATE} (W${WEEK_NUM})"

# --- Metric 1: PR Merge Rate ---
# Look for explicit stats lines in memory files, take latest
PR_STATS=""
MERGED=0
TOTAL=0
OPEN=0

for f in "${MEMORY_DIR}/${YEAR}"-*.md; do
  [ -f "$f" ] || continue
  fdate=$(basename "$f" .md)
  if [[ "$fdate" > "$START_DATE" || "$fdate" == "$START_DATE" ]] && [[ "$fdate" < "$END_DATE" || "$fdate" == "$END_DATE" ]]; then
    # Look for lines like "X open / Y merged / Z total" or "Open N, Merged N, Total N"
    line=$(grep -iE '(merged|merge rate).*total|open.*merged.*total' "$f" | tail -1 || true)
    if [ -n "$line" ]; then
      PR_STATS="$line"
      # Try to extract numbers
      m=$(echo "$line" | grep -oP '\d+(?=\s*merged)' | tail -1 || true)
      t=$(echo "$line" | grep -oP '\d+(?=\s*total)' | tail -1 || true)  
      o=$(echo "$line" | grep -oP '\d+(?=\s*open)' | tail -1 || true)
      [ -n "$m" ] && MERGED=$m
      [ -n "$t" ] && TOTAL=$t
      [ -n "$o" ] && OPEN=$o
    fi
  fi
done

if [ "$TOTAL" -gt 0 ]; then
  RATE=$(( MERGED * 100 / TOTAL ))
else
  RATE="N/A"
fi

# --- Metric 2: Unupgraded Repeat Gradients ---
# Count entries with repeat markers (repeated, √óN where N‚Č•3) that are NOT struck through
UNUPGRADED=$(grep -v '~~' "$BELIEFS" | grep -cE 'repeat|√ó[3-9]|√ó[0-9]{2}' 2>/dev/null || true)
UNUPGRADED=${UNUPGRADED:-0}
TOTAL_ENTRIES=$(grep -c '^\- ' "$BELIEFS" 2>/dev/null || true)
TOTAL_ENTRIES=${TOTAL_ENTRIES:-0}
UPGRADED=$(grep -c 'Ś∑≤ŚćáÁļß\|Ś∑≤śĮēšłö\|graduated' "$BELIEFS" 2>/dev/null || true)
UPGRADED=${UPGRADED:-0}

# --- Metric 3: Daily Audit Findings ---
AUDIT_FINDINGS=0
AUDIT_DAYS=0
for f in "${MEMORY_DIR}/${YEAR}"-*.md; do
  [ -f "$f" ] || continue
  fdate=$(basename "$f" .md)
  if [[ "$fdate" > "$START_DATE" || "$fdate" == "$START_DATE" ]] && [[ "$fdate" < "$END_DATE" || "$fdate" == "$END_DATE" ]]; then
    count=$(grep -ciE 'Śģ°Ťģ°.*ŚŹĎÁéį|audit.*found|audit.*finding|śĚ°.*ťóģťĘė|śĚ°.*ťĒôŤĮĮ' "$f" 2>/dev/null || true)
    count=${count:-0}
    if [ "$count" -gt 0 ]; then
      AUDIT_DAYS=$((AUDIT_DAYS + 1))
      # Try to extract number of findings
      while IFS= read -r n; do
        [ -n "$n" ] && AUDIT_FINDINGS=$((AUDIT_FINDINGS + n))
      done < <(grep -iE 'Śģ°Ťģ°.*ŚŹĎÁéį|audit.*found' "$f" | grep -oP '\d+(?=\s*śĚ°)' || true)
    fi
  fi
done

# --- Previous week comparison ---
PREV_WEEK_FILE="${EVAL_DIR}/${YEAR}-W$(printf '%02d' $((10#$WEEK_NUM - 1))).md"
PREV_NOTE=""
if [ -f "$PREV_WEEK_FILE" ]; then
  PREV_NOTE="(previous week report exists at $(basename "$PREV_WEEK_FILE"))"
fi

# --- Write report ---
cat > "$OUT" <<EOF
---
title: Weekly Eval W${WEEK_NUM}
period: ${START_DATE} ‚Üí ${END_DATE}
generated: $(date -u +%Y-%m-%dT%H:%M:%SZ)
---

# Weekly Eval ‚ÄĒ ${YEAR} W${WEEK_NUM}

Period: **${START_DATE}** ‚Üí **${END_DATE}**

## 1. PR Merge Rate (śČďŚ∑•Ťī®ťáŹ)

| Metric | Value |
|--------|-------|
| Open | ${OPEN} |
| Merged | ${MERGED} |
| Total | ${TOTAL} |
| Merge Rate | ${RATE}% |

Source: latest stats line from memory files
> ${PR_STATS:-No PR stats found in memory files this week}

## 2. Repeat Gradient Count (Ś≠¶šĻ†ťÄüŚļ¶)

| Metric | Value |
|--------|-------|
| Total entries in beliefs-candidates | ${TOTAL_ENTRIES} |
| Already upgraded to DNA | ${UPGRADED} |
| Unupgraded with repeat markers | ${UNUPGRADED} |

**Signal**: Lower unupgraded count = lessons being absorbed faster.

## 3. Daily Audit Findings (śČßŤ°ĆÁļ™Śĺč)

| Metric | Value |
|--------|-------|
| Days with audit findings | ${AUDIT_DAYS} |
| Total findings (approx) | ${AUDIT_FINDINGS} |

**Signal**: Decreasing findings = fewer gaps.

## Trend

${PREV_NOTE:-First report ‚ÄĒ no previous week to compare.}

## Notes

_Auto-generated by weekly-eval.sh. Review and annotate manually._
EOF

echo "‚úÖ Report written to: $OUT"
echo ""
cat "$OUT"
